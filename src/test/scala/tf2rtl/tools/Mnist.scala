  def prepareInputStream(
      arraySize: Int,
      count: Int = 1,
      cnn: Boolean = false
  ): ByteArrayInputStream =
    new ByteArrayInputStream(
      if (cnn) prepareCnnInputBytes(arraySize, count)
      else prepareMlpInputBytes(arraySize, count)
    )

  private def prepareMlpInputBytes(
      arraySize: Int,
      count: Int = 1
  ): Array[Byte] = {
    val inputPrep           = new ByteArrayOutputStream()
    val inputPrepDataStream = new DataOutputStream(inputPrep)

    def writeImage(x: Array[Float]): Unit = {
      val width = util.divCeil(ImageSize * ImageSize, arraySize)

      for (i <- 0 until width * arraySize)
        if (i < ImageSize * ImageSize)
          inputPrepDataStream.writeFloat(x(i))
        else
          inputPrepDataStream.writeFloat(0f)
    }

    for (i <- 0 until count)
      writeImage(Mnist.Images(i))

    inputPrep.toByteArray()
  }

  private def prepareCnnInputBytes(
      arraySize: Int,
      count: Int = 1
  ): Array[Byte] = {
    val inputPrep           = new ByteArrayOutputStream()
    val inputPrepDataStream = new DataOutputStream(inputPrep)

    def writeImage(x: Array[Float]): Unit = {
      for (i <- 0 until ImageSize)
        for (j <- 0 until ImageSize)
          for (k <- 0 until arraySize)
            if (k == 0)
              inputPrepDataStream.writeFloat(x(j + i * ImageSize))
            else
              inputPrepDataStream.writeFloat(0f)
    }

    for (i <- 0 until count)
      writeImage(Images(i))

    inputPrep.toByteArray()
  }

  def assertOutput(
      dataType: ArchitectureDataType,
      arraySize: Int,
      bytes: Array[Byte],
      count: Int = 1
  ): Unit = {
    val output =
      new DataInputStream(new ByteArrayInputStream(bytes))

    for (i <- 0 until count) {
      assert(
        Util.argMax(
          Util.readResult(dataType, output, arraySize, 10)
        ) == GoldenClasses(i)
      )
    }
  }

